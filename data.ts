// Fisher-Yates shuffle algorithm
function shuffle<T>(array: T[]): T[] {
  let currentIndex = array.length, randomIndex;

  while (currentIndex !== 0) {
    randomIndex = Math.floor(Math.random() * currentIndex);
    currentIndex--;
    [array[currentIndex], array[randomIndex]] = [
      array[randomIndex], array[currentIndex]];
  }

  return array;
}

// State to track available question indices
let availableIndices: number[] = [];

const resetAvailableIndices = () => {
    availableIndices = Array.from({ length: QUESTIONS.length }, (_, i) => i);
    shuffle(availableIndices);
}

// Initialize pool
resetAvailableIndices();

export const getRandomQuestion = (): Question => {
  if (availableIndices.length === 0) {
      // If all questions used, reset deck to recycle
      resetAvailableIndices();
  }
  
  // Pop a unique index
  const index = availableIndices.pop()!;
  const q = QUESTIONS[index];
  
  // Shuffle distractors and answer for display
  const options = [...q.distractors, q.answer].sort(() => Math.random() - 0.5);
  return { ...q, distractors: options.filter(o => o !== q.answer) }; 
};